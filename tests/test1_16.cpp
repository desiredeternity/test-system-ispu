#include "iotest.hpp"

int main() {
    test_exact("0 0 2 2 0 2 2 0",                         "Пересекаются");
    test_exact("0 0 1 1 2 2 3 3",                         "Не пересекаются");
    test_exact("0.0 0.0 1.5 0.0 1.5 0.0 2.5 1.2",         "Пересекаются");
    test_exact("0 0 1 0 2 0 3 0",                         "Не пересекаются");
    test_exact("0.25 0.75 2.25 1.75 0.75 1.25 2.75 0.25", "Пересекаются");
    test_contains("0 0 0 0 1 1 2 2",                      "Ошибка");
}
